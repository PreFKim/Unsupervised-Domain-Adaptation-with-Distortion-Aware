class daformer(nn.Module):
    def __init__(self,backbone,head):
        super(daformer,self).__init__()
        self.backbone = backbone
        self.classifier = head

    def forward(self,x):
        out = {}
        out['features'] = self.backbone(x)
        out['out'] = self.classifier(out['features'])

        return out